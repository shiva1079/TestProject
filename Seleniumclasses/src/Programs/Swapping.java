package Programs;

public class Swapping {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		int c;
		System.out.println(a);
		System.out.println(b);
		System.out.println("before swapping ");
		c=a;
		a=b;
		b=c;
		System.out.println("after swaping numbers are");
		System.out.println(a);
		System.out.println(b);

		
	}

}
