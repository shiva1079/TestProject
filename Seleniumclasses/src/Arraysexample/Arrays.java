package Arraysexample;

public class Arrays {
	public static void main(String[] args) {
		int num[]=new int[6];
		num[0]=10;
		num[1]=30;
		num[2]=30;
		num[3]=40;
		num[4]=50;
		num[5]=60;
		System.out.println(num[4]);
		System.out.println(num.length);
		for(int i=0;i<num.length;i++) {
			System.out.println(num[i]);
		}
		
		
		
	}

}
